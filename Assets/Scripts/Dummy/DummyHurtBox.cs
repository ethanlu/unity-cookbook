using Common.Events;
using System;
using UnityEngine;

namespace Dummy
{
    [RequireComponent(typeof(BoxCollider2D))]
    public class DummyHurtBox : MonoBehaviour
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // events
        
        public event EventHandler OnHurtEvent;
        
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // unity components

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // internal properties

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // internal methods

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // getters

        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // core unity behavior
        
        protected void OnTriggerEnter2D(Collider2D other)
        {
            if (OnHurtEvent is not null)
            {
                HurtEventArgs a = new HurtEventArgs();
                a.source = other.name;
                OnHurtEvent(this, a);
            }
        }

        protected void Awake()
        {
        }

        protected void Start()
        {
        }
        
        protected void Update()
        {
        }
        
        protected void FixedUpdate()
        {
        }
    }
}